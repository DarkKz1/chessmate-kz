// Returns evaluation in centipawns. Positive = white better.
// Mate in N is encoded as +/-(100000 - N) so closer mates rank higher.
export function evaluatePosition(chess: Chess): number {
  if (chess.isCheckmate()) {
    return chess.turn() === "w" ? -100000 : 100000;
  }
  if (chess.isDraw() || chess.isStalemate() || chess.isInsufficientMaterial()) {
    return 0;
  }

  let score = 0;
  const board = chess.board();
  for (let r = 0; r < 8; r++) {
    for (let f = 0; f < 8; f++) {
      const piece = board[r][f];
      if (!piece) continue;
      const value = PIECE_VALUES[piece.type];
      const pst = PST[piece.type];
      const positional = piece.color === "w" ? pst[r][f] : pst[7 - r][f];
      score += piece.color === "w" ? value + positional : -(value + positional);
    }
  }
  return score;
}

